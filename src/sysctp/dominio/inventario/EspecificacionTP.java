
package sysctp.dominio.inventario;

/**
 *
 * @author jarteaga
 */
public class EspecificacionTP 
{
    
    private int año;
    private long precio;   
    private int cate;
    private int tea;

    public int getAño() {
        return año;
    }

    public void setAño(int año) {
        this.año = año;
    }

    public int getCate() {
        return cate;
    }

    public void setCate(int cate) {
        this.cate = cate;
    }

    public long getPrecio() {
        return precio;
    }

    public void setPrecio(long precio) {
        this.precio = precio;
    }

    public int getTea() {
        return tea;
    }

    public void setTea(int tea) {
        this.tea = tea;
    }
    
  
  
    
}
